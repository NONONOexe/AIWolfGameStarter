package jp.ac.maslab.ando.aiwolf.starter;

/**
 * 設定項目です。
 * @author keisuke
 */
public enum SettingItem {
	/**
	 * プレイヤーの人数を示します。
	 */
	NUMBER_OF_PLAYERS,
	/**
	 * プレイヤーファイルのパスを示します。
	 */
	PLAYER_FILE,
	/**
	 * デフォルトのプレイヤークラスを示します。
	 */
	DEFAULT_PLAYER,
	/**
	 * 繰り返すゲームの数を示します。
	 */
	NUMBER_OF_GAMES,
	/**
	 * ログの保存先を示します。
	 */
	LOG_DIRECTORY;
}
