package jp.ac.maslab.ando.aiwolf.starter;

/**
 * 設定ファイルの項目です。
 * @author keisuke
 */
public enum ConfigItem {
	/**
	 * プレイヤーの人数を示します。
	 */
	NUMBER_OF_PLAYERS,
	/**
	 * プレイヤーファイルのパスを示します。
	 */
	PLAYER_FILE_PATH,
	/**
	 * デフォルトのプレイヤークラスを示します。
	 */
	DEFAULT_PLAYER_CLASS_NAME,
	/**
	 * 繰り返すゲームの数を示します。
	 */
	NUMBER_OF_GAMES,
	/**
	 * ログの保存先のディレクトリパスを示します。
	 */
	LOG_DIRECTORY_PATH;
}
